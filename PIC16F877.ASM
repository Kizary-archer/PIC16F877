
;================================================
; Настройка и конфигурация микроконтроллера
;================================================
	LIST  p=16F877
	#include <p16F877.inc> 
;================================================
; Инициализация регистров специального назначения
;================================================

INTCON			equ 0x0B
STATUS			equ 0x03
PORTD			equ 0x08
TRISD			equ 0x88

;================================================
; Инициализация констант
;================================================

RP0			equ 0x05
C           equ 0x00

;================================================
; Инициализация переменных в памяти данных
;================================================

;================================================
; Начало программы
;================================================
	ORG 0x00
	goto Start

Start:
	clrf INTCON			;запрещаем все прерывания

;===============================================
; Настраиваем линии порта PORTD на выход
;===============================================

	bsf STATUS, RP0 			;переходим в банк 1
	
	movlw b'00000000'		;помещаем в аккумулятор число 0 
	movwf TRISD			;устанавливаем линии порта 
					;PortD на выход
	movwf STATUS		;обнуляем STATUS
			

;===============================================
; Закончили настройку
;===============================================

BSF STATUS,C
movwf PORTD	

			;помещаем в аккумулятор число 0
LOOP:


	RLF PORTD,1
GOTO LOOP

	End